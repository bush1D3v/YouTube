a = 10
b = 3

print(a + b)   # Soma → 13
print(a - b)   # Subtração → 7
print(a * b)   # Multiplicação → 30
print(a / b)   # Divisão normal → 3.3333
print(a // b)  # Divisão inteira → 3
print(a % b)   # Resto da divisão → 1
print(a ** b)  # Potenciação → 1000

numero = 7

if numero % 2 == 0:
    print("Número par")
else:
    print("Número ímpar")

